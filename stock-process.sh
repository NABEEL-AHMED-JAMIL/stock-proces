#!/usr/bin/env bash

echo "*********************************************************************************************"
echo "********************************* Stock Process Clean Install *******************************"
echo "*********************************************************************************************"

# step 1
echo "mvn clean install"
mvn clean install
STATUS=$?

if [ $STATUS -eq 0 ]; then
	echo "*********************************************************************************************"
	echo "********************************* Stock Process Run *****************************************"
	echo "*********************************************************************************************"
	
	# step 8
	echo "mvn spring-boot:run"
	mvn spring-boot:run
	STATUS=$?

	if [ $STATUS -eq 0 ]; then
		echo "*********************************************************************************************"
		echo "*********************************    Build Success  *****************************************"
		echo "*********************************************************************************************"
	else
		echo "*********************************************************************************************"
		echo "*********************************    Build Fail     *****************************************"
		echo "*********************************************************************************************"
	fi
		
else
	echo "*********************************************************************************************"
	echo "*********************************    Build Fail     *****************************************"
	echo "*********************************************************************************************"
fi